    </div>
  </article>
</template>

<script>
  import {mapState, mapGetters, mapActions, mapMutations} from 'vuex';

  module.exports = {
    name: 'detail',
    data() {
      return {
        detail: {},
        showDialog: false,
        showCheckoutDialog: false,
        showRefundDialog: false,
        refundValue: null,
        pmsCheckoutId: '',
      }
    },
    computed: {
      routeId() {
        return this.$route.params.id
      },
      isPreCheckin() {
        return /precheckin/.test(this.$route.path)
      },
      isLivein() {
        return /livein/.test(this.$route.path)
      },
      isInvoice() {
        return /invoice/.test(this.$route.path)
      },
      isCheckout() {
        return /checkout/.test(this.$route.path)
      },
      isRefund() {
        return /refund/.test(this.$route.path)
      },
      isReceipt() {
        return /receipt/.test(this.$route.path)
      },
      isShowInvoiceBtn() {
        return this.isInvoice || this.isCheckout || this.isRefund
      },
    },
    methods: {
      ...mapActions([
        'goto',
        'getorderdetail',
        'conformPmsSync',
        'pmscheckout',
        'confirmInvoice',
        'refundapply',
        'setUploadStatus'
      ]),
      setuploadstatus() {
        this.setUploadStatus({
          identity_id: this.identityId,
          onsuccess: body => {
            this.getDetail();

          }
        })
      },
      isShowCheckoutDialog(id) {
        this.showCheckoutDialog = true;
        this.pmsCheckoutId = id;
      },
      //手工入账
      confirmPmsResult() {
        this.conformPmsSync({
          order_id: this.routeId,
//          action: 'SETACCOUNT',
          onsuccess: body => {
            this.detail.status.is_recording_success = true;
            this.getDetail()
          }
        })
      },
      //退款申请
      refundApply() {
        this.refundapply({
          orderId: this.routeId,
          onsuccess: body => {
            this.getDetail();
          }
        })
      },
      //确认开票
      setInvoiceConfirm() {
        this.confirmInvoice({
          invoice_apply_id: this.detail.invoice.id,
          invoice_status: 2,
          onsuccess: () => this.getDetail()
        })
      },
      //手动上传旅业系统
      setLvYeStatus(suborderId) {
        this.setUploadStatus({
          identity_id: suborderId,
          onsuccess: body => this.getDetail()
        })
      },
      //退房申请
      pmsCheckout() {
        this.pmscheckout({
          id: this.pmsCheckoutId,
          onsuccess: body => this.getDetail()
        })
      },
      getDetail() {
        this.getorderdetail({
          order_id: this.routeId,
          roomfee: 0,
          suborder: 1,
          invoice: 1,
          log: 1,
          bill: 1,
          onsuccess: body => {
            this.detail = body.data
          }
        })
      },
    },
    watch: {
      routeId(val) {
        val ? this.getDetail() : null
      }
    },
    mounted() {
      this.getDetail();
    }
  }
</script>
