      <p class="synchronize">上次同步PMS时间: {{datetimeparse(detail.update_time, 'MMDDhhmm')}}</p>
      <div class="synchronize" v-if="isPreCheckin && detail.status && !detail.status.is_recording_success">
        <span class="record-tip">入账失败</span>
